class for_loop
{
		public static void main(String args[])
		{
				//In for loop initialization,condition & inc/dec is written together
				//But In while loop it is written separately
			for(int i=1;i<=5;i++)
			{
				System.out.println(i);
	
			}
				
			
			for(int i=5;i>=1;i--)
			{
				System.out.println(i);
	
			}
				
		}
}