class Datatypes_2
{
	public static void main(String args[])
	{
		float a=12.96f;
		System.out.println("float value is "+a+" f needs to be written at the last of value, if not then system will think the value is double ");
		
		double b=25.124789421;
		System.out.println("double value is "+b+" this value is more accurate than float" );
		
	//Double is more accurate than float	
	
		char c='$';
		System.out.println(c);
		
		boolean d=true; //true or false
		System.out.print(d);
	
	}
}

